CREATE TABLE tbl_student(
 sid CHAR(7) PRIMARY KEY, -- 学籍番号
 sname VARCHAR(16) NOT NULL, -- 氏名
 labid INT NOT NULL -- 所属研究室ID
 );

CREATE TABLE tbl_lab(
 labid  INT PRIMARY KEY,
 labname VARCHAR(32),
 study VARCHAR(32)
 );

CREATE TABLE tbl_group(
 gid SERIAL PRIMARY KEY,
 gname  VARCHAR(16) NOT NULL,
 gyear INT,
 staff  VARCHAR(16),
 pdate DATE,
 room  VARCHAR(16),
 detail TEXT
 );

CREATE TABLE tbl_labgroup(
 gid INT,
 labid INT
 );


CREATE TABLE tbl_program(
 sid CHAR(7),
 title VARCHAR(64),
 time1 TIME,
 time2 TIME,
 ext VARCHAR(8)  -- 発表資料のファイル名(gid_sidとする)の拡張子（pdf,pptx)
 );


CREATE TABLE tbl_user(
 uid VARCHAR(16) PRIMARY KEY, -- ユーザID
 uname VARCHAR(32) NOT NULL, -- ユーザ名
 upass VARCHAR(16) NOT NULL, -- パスワード
 urole INT -- ユーザ種別:1-学生、2-教員、9-管理者
);



